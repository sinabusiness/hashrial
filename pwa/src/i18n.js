const LangContext = createContext();

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("hashrial_lang") || "en");

  useEffect(() => {
    document.documentElement.setAttribute("lang", lang);
    document.documentElement.dir = (LANGS.find(l => l.code === lang) || {}).dir || "ltr";
    localStorage.setItem("hashrial_lang", lang);
  }, [lang]);

  function t(key) {
    const langData = T[lang] || T.en;
    return langData[key] || T.en[key] || key;
  }

  return (
    <LangContext.Provider value={{ lang, setLang, t, langs: LANGS }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}
